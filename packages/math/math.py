import math

def acos(x):
    """Returns the arc cosine of a number"""
    return math.acos(x)
    
def acosh(x):
    """Returns the inverse hyperbolic cosine of a number"""
    return math.acosh(x)
    
def asin(x):
    """Returns the arc sine of a number"""
    return math.asin(x)
    
def asinh(x):
    """Returns the inverse hyperbolic sine of a number"""
    return math.asinh(x)
    
def atan(x):
    
    return math.atan(x)
    
def atan2(x, y):
    
    return math.atan2(x, y)
    
def atanh(x):
    
    return math.atanh(x)
    
def ceil(x):
    
    return math.ceil(x)
    
def cos(x):
    
    return math.cos(x)
    
def cosh(x):
    
    return math.cosh(x)
    
def degrees(x):
    
    return math.degrees(x)
    
def factorial(x):
    
    return math.factorial(x)
    
def fmod(x, y):
    
    return math.fmod(x, y)
    
def gamma(x):
    
    return math.gamma(x)
    
def gcd(x, y):
    
    return math.gcd(x, y)
    
def isnan(x):
    
    return math.isnan(x)
    
def sin(x):
    
    return math.sin(x)
    
def sinh(x):
    
    return math.sinh(x)
    
def tan(x):
    
    return math.tan(x)
    
def tanh(x):
    
    return math.tanh(x)
    
def e():
    
    return math.e
    
def inf():
    
    return math.inf
    
def nan():
    
    return math.nan
    
def pi():
    
    return math.pi
    
def tau():
    
    return math.tau